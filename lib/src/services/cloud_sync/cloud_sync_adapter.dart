/// Контракт облачной синхронизации (фаза 5 `plan.md`).
/// Реализация через Supabase (`SupabaseCloudSyncAdapter`) подключается отдельно, без изменения UI-ядерных моделей.
///
/// Снимок для выгрузки: метод `DatabaseService.exportGameBackupJson()`.
///
/// **Разрешение конфликтов (стратегия для продукта):**
/// - **Охотник:** при расхождении после `pullRemote` — брать снимок с более поздней меткой времени экспорта
///   (`exportedAt` в JSON) или последней успешной `pushLocal` (Last Write Wins по полному бэкапу).
/// - **Квесты:** идентификатор `Quest.id` уникален; при слиянии двух списков — объединение по `id`,
///   при конфликте полей у одного `id` — версия с более новым `updatedAt` (если добавить поле) или приоритет удалённой копии.
/// - **Инвентарь:** осторожно с дублями предметов; предпочтительно LWW на уровне целого `hunter` из бэкапа, а не по слотам.
/// Текущая реализация `pullRemote` обычно перезаписывает локальный Hive — это эквивалентно «облако победило»
/// или «последний импорт целиком»; перед сменой поведения зафиксируйте стратегию в `plan.md`.
abstract class CloudSyncAdapter {
  /// Провайдер настроен (ключи, URL, авторизация).
  bool get isConfigured;

  /// Выгрузить локальное состояние (охотник, квесты, мета).
  Future<void> pushLocal();

  /// Подтянуть облако в Hive (с осторожностью: перезапись локальных данных).
  Future<void> pullRemote();
}

/// Заглушка: только локальный режим, как в текущей сборке.
class LocalOnlyCloudSyncAdapter implements CloudSyncAdapter {
  @override
  bool get isConfigured => false;

  @override
  Future<void> pushLocal() async {}

  @override
  Future<void> pullRemote() async {}
}
